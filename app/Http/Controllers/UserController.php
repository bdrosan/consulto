    public function storeRole(Request $request)
    {
        $request->validate([
            'role' => ['required'],
        ]);

        $user = User::find($request->id);
        $user->assignRole($request->role);

        return redirect()->route('user.show', $request->id)->with('success', 'Role assigned');
    }
